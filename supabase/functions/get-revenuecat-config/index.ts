import { serve } from "https://deno.land/std@0.168.0/http/server.ts"

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    const revenueCatPublicKey = Deno.env.get('REVENUECAT_PUBLIC_KEY')

    if (!revenueCatPublicKey) {
      console.error('RevenueCat public key not configured')
      return new Response(
        JSON.stringify({ 
          error: 'RevenueCat configuration missing',
          message: 'Please configure REVENUECAT_PUBLIC_KEY in Supabase secrets'
        }),
        { 
          status: 500, 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
        }
      )
    }

    console.log('RevenueCat config requested - returning public key')

    return new Response(
      JSON.stringify({ 
        publicKey: revenueCatPublicKey,
        success: true 
      }),
      { 
        status: 200, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    )

  } catch (error) {
    console.error('Error getting RevenueCat config:', error)
    return new Response(
      JSON.stringify({ 
        error: 'Internal server error',
        message: error.message 
      }),
      { 
        status: 500, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    )
  }
})